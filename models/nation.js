const mongoose = require("mongoose");
const Schema = mongoose.Schema;

const nationSchema = new Schema(
  {
    name: {
      type: String,
      require: true,
      unique: true,
    },
    image: {
      type: String,
      require: true,
    },
    description: {
      type: String,
      require: true,
    },
  },
  {
    timestamps: true,
  }
);

const Nation = mongoose.model("nations", nationSchema);
module.exports = Nation;
